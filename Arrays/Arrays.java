public class Arrays {
    public static void main(String[] args) {
        int my_data1 = 7;
        int my_data2 = 15;
        int my_data3 = 23;
        int my_data4 = 31;
    
        int[] my_data = {7, 15, 23, 31};
    
        System.out.println("Number 1: " + my_data1);
        System.out.println("Number 2: " + my_data2);
        System.out.println("Number 3: " + my_data3);
        System.out.println("Number 4: " + my_data4);
    
        System.out.println("Array Element 1: " + my_data[0]);;
        System.out.println("Array Element 2: " + my_data[1]);;
        System.out.println("Array Element 3: " + my_data[2]);;
        System.out.println("Array Element 4: " + my_data[3]);;
    }
}